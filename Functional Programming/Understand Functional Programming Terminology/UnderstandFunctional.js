// Function that returns a string representing a cup of green tea
const prepareGreenTea = () => 'greenTea';

// Function that returns a string representing a cup of black tea
const prepareBlackTea = () => 'blackTea';

/*
Given a function (representing the tea type) and number of cups needed, the
following function returns an array of strings (each representing a cup of
a specific type of tea).
*/
const getTea = (prepareTea, numOfCups) => {
  const teaCups = [];

  for(let cups = 1; cups <= numOfCups; cups += 1) {
    const teaCup = prepareTea();
    teaCups.push(teaCup);
  }
  return teaCups;
};

// Only change code below this line
/* 
we pass the functions prepareGreenTea() and prepareBlackTea() as parameters or callback functions
for the getTea() functions being assigned to the two constant variables tea4BlackTeamFCC and tea4GreenTeamFCC
This makes it so that no global variables are changed, and we have the option to add an unlimited number
of different choices of preparedTea() methods since it's a callback function being passed to the higher order function of getTea()
*/
const tea4GreenTeamFCC = getTea(prepareGreenTea, 27);
const tea4BlackTeamFCC = getTea(prepareBlackTea, 13);
